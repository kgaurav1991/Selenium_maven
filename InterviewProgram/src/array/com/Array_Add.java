package array.com;

public class Array_Add {

	public static void main(String[] args) {
		int ar[] = { 1, 2, 3, 4 };
		int arr[] = { 5, 6 };

		int x = 0, y = 0;


		for (int i = 0; i < ar.length; i++) {
			x = ar[i];
			System.out.println(x);
		}
		for (int i = 0; i < arr.length; i++) {
			y = arr[i];
			System.out.println(y);
		}

	}

}
